// Для lessons.html
function showLesson(topic) {
    let info = document.getElementById("lesson-info");
    if (topic === "Что такое деньги?") {
        info.innerHTML = "Деньги — это то, что мы используем, чтобы покупать игрушки, еду и другие вещи. Они бывают монетами и бумажными купюрами!";
    } else if (topic === "Как копить?") {
        info.innerHTML = "Если ты хочешь большую игрушку, откладывай немного денег каждый раз, когда их получаешь. Так ты накопишь!";
    } else if (topic === "Покупки и сдача") {
        info.innerHTML = "Когда покупаешь что-то, важно считать сдачу. Например, если конфета стоит 3 рубля, а ты дал 5 рублей, тебе вернут 2 рубля!";
    }
}